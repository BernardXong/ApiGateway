    public static final CharSequence EQUAL = "=";

    /** 与和 */
    public static final CharSequence AND  = "&";

    /** 逗号 */
    public static final CharSequence COMMA = ",";

    /** 冒号 */
    public static final CharSequence COLON = ":";

    /** 空格 */
    public static final CharSequence SPACE = " ";

    /** 星号 */
    public static final CharSequence START = "*";
